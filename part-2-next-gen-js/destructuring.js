/**
 * Destructuring => Easily extract array elements or object properties and store them in variable
 */
//Array destructuring
[a, b] = ["Hello", "Anggit Prayogo"]
console.log(a)
console.log(b)
//output 
//Hello
//Anggit

//Object destructuring
const {name} = {name : "Anggit Prayogo"}
console.log(name)
//output : Anggit Prayogo